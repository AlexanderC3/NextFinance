"use client"

import { AlertDialogHeader, AlertDialog, AlertDialogContent, AlertDialogTitle, AlertDialogDescription, AlertDialogFooter, AlertDialogCancel, AlertDialogAction } from "@/components/ui/alert-dialog"
import { useMutation, useQueryClient } from "@tanstack/react-query"
import { toast } from "sonner"
import { DeleteTransaction } from "../_actions/deleteTransaction"

interface Props {
    open: boolean,
    setOpen: (open: boolean) => void,
    transactionId: string
}

function DeleteTransactionDialog({open,setOpen,transactionId} : Props) {
    const queryClient = useQueryClient()

    const deleteMutation = useMutation({
        mutationFn: DeleteTransaction,
        onSuccess: async () => {
            toast.success("Transaction deleted",{
                id: transactionId
            });

            await queryClient.invalidateQueries({
                queryKey: ["transactions"],
            });
        },
        onError: () => {
            toast.error("Something went wrong ğŸ˜µâ€ğŸ’«", {
                id: transactionId
            })
        }
    })

  return (
    <AlertDialog open={open} onOpenChange={setOpen}>
        <AlertDialogContent>
            <AlertDialogHeader>
                <AlertDialogTitle>Are you absolutely sure ?</AlertDialogTitle>
                <AlertDialogDescription>This action cannot be undone. This will permanently delete this transaction.</AlertDialogDescription>
            </AlertDialogHeader>
            <AlertDialogFooter>
                <AlertDialogCancel>Cancel</AlertDialogCancel>
                <AlertDialogAction onClick={() => {
                    toast.loading("Deleting transaction", {
                        id: transactionId
                    });
                    deleteMutation.mutate(transactionId)
                }}>Continue</AlertDialogAction>
            </AlertDialogFooter>
        </AlertDialogContent>
    </AlertDialog>
  )
}

export default DeleteTransactionDialog